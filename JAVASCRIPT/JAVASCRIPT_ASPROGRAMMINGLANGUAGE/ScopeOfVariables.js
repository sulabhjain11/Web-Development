// var, let, const are the three types of variables in javascript
//-------------UNDERSTANDING var--------------------------
console.log(x);
var x=10.8;
console.log(x);
//-------------UNDERSTANDING let--------------------------
console.log(y);
let y=10.8;
console.log(y);
//-------------UNDERSTANDING const-------------------------- same as let in this manner
console.log(z);
const z=10.8;
console.log(z);
